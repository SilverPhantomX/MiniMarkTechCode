    private void cargarCargos() {
        try {
            Connection con = new Conexion().establecerConexion();
            PreparedStatement ps = con.prepareStatement("SELECT nombreCargo FROM Cargo");
            ResultSet rs = ps.executeQuery();

            cmbCargo.removeAllItems();

            while (rs.next()) {
                cmbCargo.addItem(rs.getString("nombreCargo"));
            }

        } catch (Exception e) {
            System.out.println("Error al cargar cargos: " + e.getMessage());
        }
    }

